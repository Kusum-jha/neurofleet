public class Variables {
    public static void main(String[] args) {
        int age = 22;
        double marks = 85.5;
        char grade = 'A';
        boolean isPassed = true;

        System.out.println("Age: " + age);
        System.out.println("Marks: " + marks);
        System.out.println("Grade: " + grade);
        System.out.println("Passed: " + isPassed);
    }
}
